package OOPandCollections.Lesson5.AddAttributes;

public class AddAttributes {
    public int attributesAdd(int attr1, int attr2){
        return attr1+attr2;
    }
    public double attributesAdd(int attr1, double attr2){
        return attr1+attr2;
    }
    public double attributesAdd(double attr1, double attr2, double attr3){
        return attr1+attr2+attr3;
    }
    public String attributesAdd(String attr1, String attr2){
        return attr1+attr2;
    }
    public String attributesAdd(int attr1, String attr2){
        return attr1+attr2;
    }
}
